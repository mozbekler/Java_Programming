package day04_concatenation;

public class ArithmeticOperations {

    public static void main(String[] args) {

        System.out.println("-----Arithmetic Operations--------");

        System.out.println(17+12);
        System.out.println(17-12);
        System.out.println(17/12);
        System.out.println(17.0/12.0);  //If one value is a double value, then the result is also double
        System.out.println(17*12);
        System.out.println(17.0%12);
        System.out.println( (int) (2.5/0.5));
        System.out.println((double)(6+4)/(2*2)); //What is the problem here?


    }

}
