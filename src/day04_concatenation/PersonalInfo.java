package day04_concatenation;

public class PersonalInfo {

    public static void main(String[] args) {

        String firstName = "Mehmet";
        String lastName = "Ozbekler";
        char gender = 'M';
        byte age = 37;
        String from = "Turkey";
        String favMusic = "Classical";
        String favBook = "Faust";
        String favShow = "Guldur Guldur";
        String favColor = "Green";
        boolean isMarried = true,
                isEmployed = false;

        System.out.println("My name is "+ firstName +" "+ lastName);


        System.out.println("firstName = " + firstName);
        System.out.println("lastName = " + lastName);
        System.out.println("gender = " + gender);
        System.out.println("age = " + age);
        System.out.println("from = " + from);
        System.out.println("favMusic = " + favMusic);
        System.out.println("favBook = " + favBook);
        System.out.println("favShow = " + favShow);
        System.out.println("favColor = " + favColor);
        System.out.println("isMarried = " + isMarried);
        System.out.println("isEmployed = " + isEmployed);


    }

}
