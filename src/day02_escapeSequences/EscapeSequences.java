package day02_escapeSequences;

public class EscapeSequences {

    public static void main(String[] args) {

        System.out.println("Hello students.\nToday we have Java Class"); // \n sequence adds a new line

        System.out.println("---------------------------");

        System.out.println("\tJava Programming Language");
        System.out.println("\t\t\t\tJava Developer Course Batch 1");

        System.out.println("---------------------------");
        System.out.println("\\");

        System.out.println("---------------------------");

        System.out.println(" MY favorite movie is \"The Gladiator\"");





    }



}

/*
    1.\n: new line
    2.\t: Paragraph space
    3.\\: Single back slash
    4.\": double quote

 */