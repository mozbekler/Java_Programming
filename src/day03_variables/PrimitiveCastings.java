public class PrimitiveCastings {

    public static void main(String[] args) {
        
        //implicit casting

int a = 120;

double b = a;


        System.out.println("b = " + b);
        System.out.println("----------------------------------");
        // explicit casting
        
         double x= 22.8;
         
         int y = (int) x;

        System.out.println("y = " + y);
         
         
        
        

    }
}


